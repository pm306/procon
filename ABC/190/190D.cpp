vector<long long> enum_divisors(long long N) {
    vector<long long> res;
    for (long long i = 1; i * i <= N; ++i) {
        if (N % i == 0) {
            res.push_back(i);
            if (N/i != i) res.push_back(N/i);
        }
    }
    sort(res.begin(), res.end());
    return res;
}

int main(){
    ll n; cin >> n;
    vector<ll> ed = enum_divisors(n*2);
    int cnt = 0;
    for(auto a:ed){
        ll b = 2*n/a;
        if(a%2 != b%2)cnt++;
    }
    cout << cnt << ln;
}

