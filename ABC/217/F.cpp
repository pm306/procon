using mint = modint998244353;

const int MAX = 1000;
const int MOD =998244353;

long long fac[MAX], finv[MAX], inv[MAX];
void COMinit(){
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for(int i = 2; i < MAX; i++){
        fac[i] = fac[i-1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD/i) % MOD;
        finv[i] = finv[i-1] * inv[i] % MOD;
    }
}
long long Com(int n, int k){
if(n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n-k] % MOD) % MOD;
}
long long Perm(int n, int k){
    if(n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * finv[n-k] % MOD;
}
long long HCom(int n, int k){ //重複組み合わせ
    return Com(n+k-1, k);
}

int main(){
    COMinit();
    int n, m; cin >> n >> m;
    int num = n * 2;

    vector fri(num, vector<int>(num));
    rep(i, m){
        int a, b; cin >> a >> b; a--, b--;
        fri[a][b] = fri[b][a] = true;
    }

    int memo[440][440];
    memset(memo, -1, sizeof memo);
    auto dp = [&](auto dp, int l, int r) -> mint{
        if(l > r) return 1;
        if(r%2 == l%2) return 0;
        if(memo[l][r] != -1) return memo[l][r];

        mint ret = 0;
        for(int k=l+1; k<=r; k++){
            if(fri[l][k] == false)continue;
            ret += dp(dp, l+1, k-1) * dp(dp, k+1, r) * Com((r-l+1)/2, (r-k)/2);
        }
        return memo[l][r] = ret.val();
    };

    cout << dp(dp, 0, num-1).val() << ln;
}

