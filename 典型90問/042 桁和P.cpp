/*
桁和がK(K%9=0)の自然数の組み合わせを求めよ

桁和なので思わず桁dpしそうになるが、
dp[i][k]:= 取りうる最大の数を前からi桁見たとき、桁和がkである場合の数
とするとO(1e5*K)かかってしまう
これでめっちゃ悩んだ

求めるのは9の倍数であり、1~Nの倍数ではない、というところが桁DPではないポイントだろうか？
実は
dp[i]:=桁和がiである自然数の組み合わせ
とすると
dp[i] = dp[i-1] + dp[i-2] + ... + dp[max(0, i-9)]
桁和がiの総数 == 桁の先頭が1~9 and 桁和がi-(1~9)の総数　と言える
というフィボナッチ的な漸化式を立てることができる
dp[0] = 1なのであとはforループなり再帰なりで解けばOK
*/

template<int MOD> struct Fp {
    long long val;
    constexpr Fp(long long v = 0) noexcept : val(v % MOD) {
        if (val < 0) v += MOD;
}
    constexpr int getmod() { return MOD; }
    constexpr Fp operator - () const noexcept {
        return val ? MOD - val : 0;
}
    constexpr Fp operator + (const Fp& r) const noexcept { return Fp(*this) += r; }
    constexpr Fp operator - (const Fp& r) const noexcept { return Fp(*this) -= r; }
    constexpr Fp operator * (const Fp& r) const noexcept { return Fp(*this) *= r; }
    constexpr Fp operator / (const Fp& r) const noexcept { return Fp(*this) /= r; }
    constexpr Fp& operator += (const Fp& r) noexcept {
        val += r.val;
        if (val >= MOD) val -= MOD;
        return *this;
}
constexpr Fp& operator -= (const Fp& r) noexcept {
        val -= r.val;
        if (val < 0) val += MOD;
        return *this;
}
    constexpr Fp& operator *= (const Fp& r) noexcept {
        val = val * r.val % MOD;
        return *this;
    }
    constexpr Fp& operator /= (const Fp& r) noexcept {
        long long a = r.val, b = MOD, u = 1, v = 0;
        while (b) {
            long long t = a / b;
            a -= t * b; swap(a, b);
            u -= t * v; swap(u, v);
    }
        val = val * u % MOD;
        if (val < 0) val += MOD;
        return *this;
}
    constexpr bool operator == (const Fp& r) const noexcept {
        return this->val == r.val;
    }
    constexpr bool operator != (const Fp& r) const noexcept {
        return this->val != r.val;
    }
    friend constexpr ostream& operator << (ostream &os, const Fp<MOD>& x) noexcept {
        return os << x.val;
    }
    friend constexpr istream& operator >> (istream &is, Fp<MOD>& x) noexcept {
         return is >> x.val;
    }
    friend constexpr Fp<MOD> modpow(const Fp<MOD> &a, long long n) noexcept {
        if (n == 0) return 1;
        auto t = modpow(a, n / 2);
        t = t * t;
        if (n & 1) t = t * a;
        return t;
    }
};

using mint = Fp<MOD>;

mint dp[101010];
int main(){
    int K; cin >> K;
    if(K%9!=0){
        cout << 0 << ln;
        return 0;
    }
    dp[0] = 1;
    for(int i=1; i<=K; i++){
        for(int j=i-1; j>=max(0, i-9); j--){
            dp[i] += dp[j];
        }
    }
    cout << dp[K] << ln;
}
